package org.example.tryout;

public class Calculator {

    /**
     * author John Doe
     * version 1.0
     * description are String
     */
    String name = "Calculator";


    /**
     * @param a a is the first number
     * @param b b is the second number
     * @return the sum of a and b
     * @author John Doe
     * @version 1.0
     * @description This is a simple calculator class that performs basic arithmetic operations.
     * this method adds two numbers and returns the result
     * @since 2023-10-01
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * @param a a is the first number
     * @param b b is the second number
     * @return the difference of a and b
     * @author John Doe
     * @version 1.0
     * @description This is a simple calculator class that performs basic arithmetic operations.
     * this method subtracts two numbers and returns the result
     * @since 2023-10-01
     */
    public int subtract(int a, int b) {
        return a - b;
    }


}
